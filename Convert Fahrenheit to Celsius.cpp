#include <iostream>
using namespace std;

int main() {
    double fahrenheit, celsius;

    
    cout << "input fahrenheit (Fahrenheit): ";
    cin >> fahrenheit;

   
    celsius = (fahrenheit - 32) * 5.0 / 9.0;

    
    cout << "Output celsius: " << celsius << " C" << endl;

    return 0;
}




    


    
